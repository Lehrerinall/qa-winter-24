package model;

public class Shop {
    private String adress;
    private String specialization;
    private String areaOfShop;
    private String numberOfCashDesks;
    private String workingHour;

    public String getAdress() {
        return adress;
    }

    public void setAdress(String adress) {
        this.adress = adress;
    }

    public String getSpecialization() {
        return specialization;
    }

    public void setSpecialization(String specialization) {
        this.specialization = specialization;
    }

    public String getAreaOfShop() {
        return areaOfShop;
    }

    public void setAreaOfShop(String areaOfShop) {
        this.areaOfShop = areaOfShop;
    }

    public String getNumberOfCashDesks() {
        return numberOfCashDesks;
    }

    public void setNumberOfCashDesks(String numberOfCashDesks) {
        this.numberOfCashDesks = numberOfCashDesks;
    }

    public String getWorkingHour() {
        return workingHour;
    }

    public void setWorkingHour(String workingHour) {
        this.workingHour = workingHour;
    }
}
