package model;

public class Air {
    private String airComposition;
    private String atmosphericPressure;
    private String temperature;
    private String airHumidity;
    private String speedOfMovementOfAirMasses;

    public String getAirComposition() {
        return airComposition;
    }

    public void setAirComposition(String airComposition) {
        this.airComposition = airComposition;
    }

    public String getAtmosphericPressure() {
        return atmosphericPressure;
    }

    public void setAtmosphericPressure(String atmosphericPressure) {
        this.atmosphericPressure = atmosphericPressure;
    }

    public String getTemperature() {
        return temperature;
    }

    public void setTemperature(String temperature) {
        this.temperature = temperature;
    }

    public String getAirHumidity() {
        return airHumidity;
    }

    public void setAirHumidity(String airHumidity) {
        this.airHumidity = airHumidity;
    }

    public String getSpeedOfMovementOfAirMasses() {
        return speedOfMovementOfAirMasses;
    }

    public void setSpeedOfMovementOfAirMasses(String speedOfMovementOfAirMasses) {
        this.speedOfMovementOfAirMasses = speedOfMovementOfAirMasses;
    }
}
