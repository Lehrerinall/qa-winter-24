package model;

public class Idea {
    private String branchOfScience;
    private String applicationArea;
    private String economicEfficiency;
    private String massCharacter;
    private String environmentalFriendliness;

    public String getBranchOfScience() {
        return branchOfScience;
    }

    public void setBranchOfScience(String branchOfScience) {
        this.branchOfScience = branchOfScience;
    }

    public String getApplicationArea() {
        return applicationArea;
    }

    public void setApplicationArea(String applicationArea) {
        this.applicationArea = applicationArea;
    }

    public String getEconomicEfficiency() {
        return economicEfficiency;
    }

    public void setEconomicEfficiency(String economicEfficiency) {
        this.economicEfficiency = economicEfficiency;
    }

    public String getMassCharacter() {
        return massCharacter;
    }

    public void setMassCharacter(String massCharacter) {
        this.massCharacter = massCharacter;
    }

    public String getEnvironmentalFriendliness() {
        return environmentalFriendliness;
    }

    public void setEnvironmentalFriendliness(String environmentalFriendliness) {
        this.environmentalFriendliness = environmentalFriendliness;
    }
}
